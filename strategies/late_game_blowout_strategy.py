class BlowoutSide(Enum):
    """Which team is leading (original Side enum, renamed to avoid clash with core.order_manager.Side)."""

    HOME = "home"
    AWAY = "away"


@dataclass
class BlowoutSignal:
    """Signal generated by late game blowout strategy."""

    timestamp: float
    game_id: str
    leading_team: BlowoutSide
    score_differential: int  # Always positive (leader's advantage)
    home_score: int
    away_score: int
    period: int
    time_remaining_seconds: int
    win_probability: float  # Estimated P(leader wins)
    confidence: str  # 'medium', 'high', 'very_high'

    @property
    def trailing_team(self) -> BlowoutSide:
        return (
            BlowoutSide.AWAY
            if self.leading_team == BlowoutSide.HOME
            else BlowoutSide.HOME
        )


