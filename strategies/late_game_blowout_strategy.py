"""Late Game Blowout Strategy - I_Strategy implementation.

Activates in the last 10 minutes of a game when one team has a commanding lead.
This is a low-risk strategy since teams rarely come back from large deficits late.

Entry criteria:
- Q4 (or OT) with <= 10 minutes remaining
- Score differential >= threshold (default 12 points)
- Buy YES on the leading team

Risk profile: Very low - historical comeback rates from 10+ down with <10 min are ~5%

Ported from src/strategies/late_game_blowout.py to implement I_Strategy interface.
"""

