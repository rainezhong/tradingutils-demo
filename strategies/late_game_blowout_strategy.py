def calculate_stop_loss_from_stats(
    win_rate: float,
    avg_win: float,
    avg_contracts: float = 5,
    avg_entry_price: float = 0.90,
    avg_entry_fee: float = 0.0,
    avg_exit_fee: float = 0.0,
    safety_margin: float = 0.7,
) -> Dict:
    """
    Calculate optimal price stop loss from historical win rate and P&L.

    The breakeven condition for a strategy is:
        win_rate * avg_win = (1 - win_rate) * max_loss_per_trade

    Solving for max_loss gives the largest stop loss that keeps the strategy
    profitable. A safety margin is applied to create a buffer.

    The dollar loss on a stopped trade is:
        loss = (stop_cents / 100) * contracts + entry_fee + exit_fee

    So we invert that to get stop_cents from the dollar amount.
    Cents are capped at entry_price (can't lose more than you paid).

    Args:
        win_rate: Historical win rate (0.0 to 1.0)
        avg_win: Average profit per winning trade (dollars, after fees)
        avg_contracts: Average contracts per trade (for cents conversion)
        avg_entry_price: Average entry price (for capping cents at max possible loss)
        avg_entry_fee: Average entry fee per trade (dollars)
        avg_exit_fee: Average exit fee on stopped trades (dollars)
        safety_margin: Fraction of breakeven to use (default 0.7 = 30% buffer)

    Returns:
        Dict with breakeven and recommended stop loss in dollars and cents
    """
    if win_rate <= 0 or win_rate >= 1 or avg_win <= 0:
        return {
            "error": "Need win_rate in (0,1) and avg_win > 0",
            "win_rate": win_rate,
            "avg_win": avg_win,
        }

    # Max loss per trade at breakeven
    breakeven_loss = (win_rate / (1 - win_rate)) * avg_win

    # Cap at max possible loss (lose entire position + entry fee)
    max_possible_loss = avg_entry_price * avg_contracts + avg_entry_fee
    breakeven_capped = breakeven_loss > max_possible_loss

    # Apply safety margin
    recommended_loss = breakeven_loss * safety_margin

    # Convert to cents below entry price
    # loss = (stop_cents/100) * contracts + entry_fee + exit_fee
    # stop_cents = (loss - entry_fee - exit_fee) / contracts * 100
    # Cap at entry_price in cents (can't drop below $0)
    max_cents = avg_entry_price * 100
    avg_fees = avg_entry_fee + avg_exit_fee
    if avg_contracts > 0:
        breakeven_cents = min(
            max_cents, max(0, (breakeven_loss - avg_fees) / avg_contracts * 100)
        )
        recommended_cents = min(
            max_cents, max(0, (recommended_loss - avg_fees) / avg_contracts * 100)
        )
    else:
        breakeven_cents = 0
        recommended_cents = 0

    return {
        "win_rate": win_rate,
        "avg_win": avg_win,
        "breakeven_loss_dollars": round(min(breakeven_loss, max_possible_loss), 2),
        "recommended_loss_dollars": round(min(recommended_loss, max_possible_loss), 2),
        "breakeven_cents": round(breakeven_cents, 1),
        "recommended_cents": round(recommended_cents, 1),
        "breakeven_capped": breakeven_capped,
        "safety_margin": safety_margin,
        "avg_contracts": avg_contracts,
        "avg_entry_price": avg_entry_price,
        "avg_fees_per_trade": round(avg_fees, 2),
    }


def backtest_blowout_strategy(
    game_snapshots: List[Dict],
    config: Optional[LateGameBlowoutConfig] = None,
) -> Dict:
    """
    Backtest the blowout strategy on recorded game data.

    Args:
        game_snapshots: List of game state snapshots with keys:
            - timestamp, home_score, away_score, period, time_remaining
            - home_price, away_price (optional)
            - game_result: 'home' or 'away' (who won)
        config: Strategy configuration

    Returns:
        Dict with backtest results
    """
    strategy = LateGameBlowoutStrategy(config=config)

    trades = []
    pnl = 0.0
    wins = 0
    losses = 0

    # Track if we've already traded this game
    traded_games: set = set()

    for snap in game_snapshots:
        game_id = snap.get("game_id", "unknown")

        # Skip if already traded this game
        if game_id in traded_games:
            continue

        blowout_signal = strategy._check_entry(
            home_score=snap["home_score"],
            away_score=snap["away_score"],
            period=snap["period"],
            time_remaining=snap["time_remaining"],
            timestamp=snap["timestamp"],
            game_id=game_id,
            home_price=snap.get("home_price"),
            away_price=snap.get("away_price"),
        )

        if blowout_signal:
            traded_games.add(game_id)

            # Determine if trade won
            game_result = snap.get("game_result")
            if game_result:
                position_size = strategy._get_position_size(blowout_signal.confidence)

                # Get the price we bought at
                if blowout_signal.leading_team == BlowoutSide.HOME:
                    buy_price = snap.get("home_price", 0.90)
                else:
                    buy_price = snap.get("away_price", 0.90)

                # Did we win?
                if (
                    blowout_signal.leading_team == BlowoutSide.HOME
                    and game_result == "home"
                ) or (
                    blowout_signal.leading_team == BlowoutSide.AWAY
                    and game_result == "away"
                ):
                    # Won - profit is (1 - buy_price) * position_size
                    trade_pnl = (1.0 - buy_price) * position_size
                    wins += 1
                else:
                    # Lost - lose entire position
                    trade_pnl = -buy_price * position_size
                    losses += 1

                pnl += trade_pnl

                trades.append(
                    {
                        "timestamp": snap["timestamp"],
                        "game_id": game_id,
                        "leading_team": blowout_signal.leading_team.value,
                        "score_diff": blowout_signal.score_differential,
                        "time_remaining": blowout_signal.time_remaining_seconds,
                        "confidence": blowout_signal.confidence,
                        "buy_price": buy_price,
                        "position_size": position_size,
                        "result": "win" if trade_pnl > 0 else "loss",
                        "pnl": trade_pnl,
                    }
                )

    total_trades = wins + losses
    win_rate = wins / total_trades if total_trades > 0 else 0

    return {
        "total_trades": total_trades,
        "wins": wins,
        "losses": losses,
        "win_rate": win_rate,
        "total_pnl": pnl,
        "avg_pnl_per_trade": pnl / total_trades if total_trades > 0 else 0,
        "trades": trades,
    }


